package br.com.projeto.api.model;

public class PessoaModel {

    // Atributo
    private String nome;
    private int idade;

    // Get e Set
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
